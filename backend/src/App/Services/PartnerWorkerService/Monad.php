   */
  private function prepareDataForPartner(array $lead): array
  {
    $secondPhone = (isset($lead['second_phone']) && null != $lead['second_phone']) ?
      $lead['second_phone'] : null;

    return [
      'api_key' => $this->apiKey,
      'name' => $lead['name'],
