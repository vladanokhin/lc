  <p>Ваша ссылка готова к использованию:
  <ul>
    <li>Убедитесь , что страницы выглядят корректно , оставьте тестовый лид</li>
    <li>Замените исходную ссылку кампании на новую , в личном кабинете рекламных платформ</li>
    <li>&#128293; <b>наливайте траф</b> &#128293;</li>
